extension ViewController: MFMailComposeViewControllerDelegate { //Ð¿Ð¾Ð´Ð¿Ð¸ÑaÑÑ Ð½Ð° delegat
    
    func mailComposeController(_ controller: MFMailComposeViewController, didFinishWith result: MFMailComposeResult, error: Error?) {
        switch result {
        case .sent:
            print("Email sent")
        case .saved:
            print("Draft saved")
        case .cancelled:
            print("Email cancelled")
        case  .failed:
            print("Email failed")
        }
        controller.dismiss(animated: true, completion: nil)
    }
    
}

